package com.ngt.jopenmetaverse.shared.exception.asset;

public class AssetPrimitiveParsingException extends Exception{

	public AssetPrimitiveParsingException() {
		super();
	}

	public AssetPrimitiveParsingException(String message, Throwable cause) {
		super(message, cause);
	}

	public AssetPrimitiveParsingException(String message) {
		super(message);
	}

	public AssetPrimitiveParsingException(Throwable cause) {
		super(cause);
	}
}
